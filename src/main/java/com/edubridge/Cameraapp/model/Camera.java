package com.edubridge.Cameraapp.model;

public class Camera {
	private int serial_number;
	private String camera_brand;
	private String camera_model;
	private String storage_capacity;
	//getters and setters methods
	public int getSerial_number() {
		return serial_number;
	}
	public void setSerial_number(int serial_number) {
		this.serial_number = serial_number;
	}
	public String getCamera_brand() {
		return camera_brand;
	}
	public void setCamera_brand(String camera_brand) {
		this.camera_brand = camera_brand;
	}
	public String getCamera_model() {
		return camera_model;
	}
	public void setCamera_model(String camera_model) {
		this.camera_model = camera_model;
	}
	public String getStorage_capacity() {
		return storage_capacity;
	}
	public void setStorage_capacity(String storage_capacity) {
		this.storage_capacity = storage_capacity;
	}
	

}
